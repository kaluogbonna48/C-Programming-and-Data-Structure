#include <stdio.h>

/**
 * Bitwise Operator
 * << - (Bitwise Left Shift)- shifts 
 * bits (binary equivalant) to left and 
 * returns the decimal equivalant of the binary formed.
 */
 
int main() {
    char var = 5; 
    printf("%d\n", var<<1);
    printf("%d\n", var<<2);
    printf("%d\n", var<<3);
    printf("%d\n", var<<4);

    return 0;
}
